public class Ausdruecke {

    public static void main( String args[] ) {
        //<<Schreibe das Ergebnis von 1024 - 621 auf die Konsole>>
        System.out.println("Schreibe das Ergebnis von 1024 - 621 auf die Konsole");
        System.out.println(1024 - 621);
        //<<Schreibe das Ergebnis von 22/7 (Ganzzahl-Division) auf die Konsole>>
        System.out.println("Schreibe das Ergebnis von 22/7 (Ganzzahl-Division) auf die Konsole");
        System.out.println(22/7);
        //<<Schreibe das Ergebnis von 22%7 (Restwert) auf die Konsole>>
        System.out.println("Schreibe das Ergebnis von 22/7 (Ganzzahl-Division) auf die Konsole");
        System.out.println(22%7);
        //<<Schreibe das Ergebnis von 91.23 + 18.21 auf die Konsole>>
        System.out.println("Schreibe das Ergebnis von 91.23 + 18.21 auf die Konsole");
        System.out.println(91.23 + 18.21);
        //<<Schreibe das Ergebnis von 128 % 2 == 0 auf die Konsole>>
        System.out.println("Schreibe das Ergebnis von 128 % 2 == 0 auf die Konsole");
        System.out.println("128 % 2 == 0");
        //<<Schreibe das Ergebnis von 7 > 1 && 7 < 19 auf die Konsole>>
        System.out.println("Schreibe das Ergebnis von 7 > 1 && 7 < 19 auf die Konsole");
        System.out.println(7 > 1 && 7 < 19);
        //<<Schreibe das Ergebnis von -1.0 < 0.5 || -1.0 > 10.2 auf die Konsole>>
        System.out.println("Schreibe das Ergebnis von -1.0 < 0.5 || -1.0 > 10.2 auf die Konsole");
        System.out.println(-1.0 < 0.5 || -1.0 > 10.2);
    }

}


